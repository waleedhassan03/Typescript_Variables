// Const Variable in typescript
// in const variable we cannot make the variables with same names or we cannot reassign it 
// if we have given the variable a name like "msg1" than it is now fixed and we cannot make another variable with the same name

const msg1 = "hello waleed";
console.log(msg1);      

const msg2 = "today is very cold day";
console.log(msg2);

