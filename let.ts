// The use and concept of variables in typescript:
// A variable is “a named space in the memory” that stores values. In other words, it acts as a container where we keep and store our data. below are the variables used in typescript

// in typescropt you can use variables using "let", "var", and "const"

// By using let
let message = "Hello, Waleed Hassan";
console.log(message);

message = "How are you doing today ?";   //in let variable we can reassign the value new values
console.log(message);

let nationality = "Pakistani";
nationality = "American";         //here is another exmaple of reassigning the new value in let variable
console.log(nationality);




























